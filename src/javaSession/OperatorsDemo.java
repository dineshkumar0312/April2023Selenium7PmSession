package javaSession;

public class OperatorsDemo {

	public static void main(String[] args) {

		int i1 = 10;
		int i2 = 20;

		System.out.println(i1 + i2);

		double d1 = 14.5;
		double d2 = 10.5;

		System.out.println(d1 - d2);

		float f1 = 11.0F;
		float f2 = 5.0f;

		System.out.println(f1 * f2);

		short s1 = 125;
		short s2 = 5;

		System.out.println(s1 / s2);

		System.out.println(s1 % s2);

		long l1 = 122;
		long l2 = 100000;

		System.out.println(l1 / l2);

		System.out.println(l1 % l2);

	}

}
