package javaSession;

public class ShorthandOperatorDemo {

	public static void main(String[] args) {

		int x = 10;

		x += 10;

		System.out.println(x);

		int y = 20;

		y -= 5;

		System.out.println(y);

		int l = 100;
		l *= 3;

		System.out.println(l);
		int y1 = 1000;

		y1 /= 10;

		System.out.println(y1);

		int k = 15;

		k %= 100;

		System.out.println(k);

	}

}
