package javaSession;

public class RelationalOperatorDemo {

	public static void main(String[] args) {

		int x = 10;
		int y = 20;

		System.out.println(x > y);
		System.out.println(x >= y);

		int m = 100;

		int n = 100;

		System.out.println(m < n);
		System.out.println(m <= n);

		long l1 = 100;
		long l2 = 200;

		System.out.println(l1 == l2);
		System.out.println(l1 != l2);

	}

}
